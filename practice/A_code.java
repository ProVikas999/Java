package practice;
import java.util.*;

public class A_code {

	public static void main(String[] args) {
	Scanner S = new Scanner(System.in);
	int n = S.nextInt();
	int i = 1;
	while (i<=n) {
		int j = 1;
		while (j<=n) {
			System.out.print("*");
			
			j++;
		}
		System.out.println();
	    i++;
	}
	}
}

